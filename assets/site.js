let lastResults = [];

const getText = (lang, key) => translations[lang]?.[key] ?? translations.en[key] ?? "";

function locationMatches(selected, itemLocation) {
  if (selected === "all") {
    return true;
  }

  const aliases = {
    kochi: ["kochi", "edappally", "kakkanad", "infopark"],
    kakkanad: ["kakkanad", "infopark"],
    infopark: ["infopark", "kakkanad"],
    edappally: ["edappally", "kochi"],
    remote: ["remote"]
  };

  return (aliases[selected] ?? [selected]).includes(itemLocation);
}

function applyTranslations(lang) {
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = getText(lang, key);
    if (element.dataset.i18nHtml === "true") {
      element.innerHTML = value;
    } else {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    element.setAttribute("placeholder", getText(lang, key));
  });
}

function renderResults(items, lang) {
  const results = document.getElementById("searchResults");
  const label = document.getElementById("searchResultsLabel");

  if (!results || !label) {
    return;
  }

  results.innerHTML = "";
  label.textContent = getText(lang, "searchResultsLabel");

  if (!items.length) {
    const empty = document.createElement("div");
    empty.className = "result-card result-card-empty";
    empty.textContent = getText(lang, "searchNoResults");
    results.appendChild(empty);
    return;
  }

  items.slice(0, 6).forEach((item) => {
    const card = document.createElement("a");
    card.className = "result-card";
    card.href = item.href;
    card.innerHTML = `
      <strong>${item.title}</strong>
      <span>${item.keywords.slice(0, 3).join(" • ")}</span>
    `;
    results.appendChild(card);
  });
}

function initSearch() {
  const form = document.getElementById("jobSearchForm");
  const languageSelect = document.getElementById("languageSelect");

  if (!form || !languageSelect) {
    return;
  }

  form.addEventListener("submit", (event) => {
    event.preventDefault();

    const lang = languageSelect.value;
    const keyword = document.getElementById("searchKeyword").value.trim().toLowerCase();
    const type = document.getElementById("searchType").value;
    const location = document.getElementById("searchLocation").value;
    const experience = document.getElementById("searchExperience").value;

    const filtered = searchIndex.filter((item) => {
      const keywordMatch =
        !keyword ||
        item.title.toLowerCase().includes(keyword) ||
        item.keywords.some((entry) => entry.includes(keyword));
      const typeMatch = type === "all" || item.type === type;
      const locationMatch = locationMatches(location, item.location);
      const experienceMatch =
        experience === "all" ||
        item.experience === experience ||
        item.experience === "all";

      return keywordMatch && typeMatch && locationMatch && experienceMatch;
    });

    lastResults = filtered;
    renderResults(filtered, lang);
    document.getElementById("searchResultsWrap")?.scrollIntoView({
      behavior: "smooth",
      block: "nearest"
    });
  });
}

function initLanguage() {
  const languageSelect = document.getElementById("languageSelect");

  if (!languageSelect) {
    return;
  }

  const updateLanguage = () => {
    const lang = languageSelect.value;
    applyTranslations(lang);
    if (lastResults.length) {
      renderResults(lastResults, lang);
    }
  };

  languageSelect.addEventListener("change", updateLanguage);
  updateLanguage();
}

document.addEventListener("DOMContentLoaded", () => {
  initLanguage();
  initSearch();
});
